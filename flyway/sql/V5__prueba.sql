/* ==========================================================
   NUMBERS HELPER TABLE
   Robust, deterministic sequence generator (1..250000) used by
   all batched loops instead of relying on sys.all_objects sizes.
   ========================================================== */
IF OBJECT_ID('tempdb..#Numbers') IS NOT NULL DROP TABLE #Numbers;

CREATE TABLE #Numbers (n INT NOT NULL PRIMARY KEY);

;WITH L0 AS (SELECT 1 AS c UNION ALL SELECT 1),                          -- 2
 L1 AS (SELECT 1 AS c FROM L0 a CROSS JOIN L0 b),                        -- 4
 L2 AS (SELECT 1 AS c FROM L1 a CROSS JOIN L1 b),                        -- 16
 L3 AS (SELECT 1 AS c FROM L2 a CROSS JOIN L2 b),                        -- 256
 L4 AS (SELECT 1 AS c FROM L3 a CROSS JOIN L3 b),                        -- 65536
 L5 AS (SELECT 1 AS c FROM L4 a CROSS JOIN L1 b)                         -- 262144
INSERT INTO #Numbers (n)
SELECT TOP (250000) ROW_NUMBER() OVER (ORDER BY (SELECT NULL))
FROM L5;

