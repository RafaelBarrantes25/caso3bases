        WHEN 3 THEN @payStatusCancelled
        WHEN 4 THEN @payStatusInsufficientFunds
        ELSE @payStatusApproved
    END AS paymentStatusId,
    @minPlayerId + ((ABS(CHECKSUM(pr.id, 'PAYER')) % @playerCount)) AS userId,
    @ptsId,
    CAST(1 + (ABS(CHECKSUM(pr.id, 'AMT')) % 1) AS DECIMAL(18,2)) AS amount, -- MAX_POINTS_PER_PREDICTION = 1
    'PROPOSITION',
    pr.id,
