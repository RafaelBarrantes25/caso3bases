PRINT 'Seeding propositions...';

DECLARE @existingPropCount BIGINT = (SELECT COUNT(*) FROM propositions);
DECLARE @p INT = 1;

WHILE @p <= @PROPOSITION_COUNT
BEGIN
    ;WITH gen AS (
        SELECT
            num.n AS n,
            -- pseudo-random but deterministic per row
            ABS(CHECKSUM(num.n, 'A')) AS r1,
            ABS(CHECKSUM(num.n, 'B')) AS r2,
            ABS(CHECKSUM(num.n, 'C')) AS r3,
            ABS(CHECKSUM(num.n, 'D')) AS r4
        FROM #Numbers num
        WHERE num.n > @p - 1
          AND num.n <= @p - 1 + @PROPOSITION_BATCH
          AND num.n <= @PROPOSITION_COUNT
    )
    INSERT INTO propositions (
        title, description, proposedBy, proposedTo, propositionStatusId, selectedVoteId,
        votingStartsAt, votingClosesAt, acceptedAt, rejectedAt, challengeStartsAt, challengeEndsAt,
        enabled, createdAt, updatedAt, checksum
    )
    SELECT
        CONCAT('Player ', ((g.r2 % @playerCount)), ' ', tv.verb) AS title,
        CONCAT('Auto-generated proposition #', n, ' for week-1 bulk seed validation. Source action: ', tv.verb, '.') AS description,
        @minPlayerId + (g.r1 % @playerCount) AS proposedBy,
        @minPlayerId + ((g.r1 + 1 + (g.r2 % (@playerCount - 1))) % @playerCount) AS proposedTo,
        CASE (n % 10)
            WHEN 0 THEN @statusProposed
            WHEN 1 THEN @statusUnderReview
            WHEN 2 THEN @statusVoting
            WHEN 3 THEN @statusSelected
            WHEN 4 THEN @statusAccepted
            WHEN 5 THEN @statusRejected
            WHEN 6 THEN @statusActive
            WHEN 7 THEN @statusCompleted
            WHEN 8 THEN @statusCancelled
            ELSE @statusUnverifiable
        END AS propositionStatusId,
        NULL AS selectedVoteId,
        DATEADD(MINUTE, -(n * 3), @now) AS votingStartsAt,
        DATEADD(MINUTE, -(n * 3) + 60, @now) AS votingClosesAt,
        CASE WHEN n % 10 IN (4,6,7) THEN DATEADD(MINUTE, -(n * 3) + 65, @now) ELSE NULL END AS acceptedAt,
        CASE WHEN n % 10 = 5 THEN DATEADD(MINUTE, -(n * 3) + 65, @now) ELSE NULL END AS rejectedAt,
        CASE WHEN n % 10 IN (6,7,8,9) THEN DATEADD(MINUTE, -(n * 3) + 70, @now) ELSE NULL END AS challengeStartsAt,
        CASE WHEN n % 10 IN (6,7,8,9) THEN DATEADD(DAY, 7, DATEADD(MINUTE, -(n * 3) + 70, @now)) ELSE NULL END AS challengeEndsAt,
        1,
        DATEADD(MINUTE, -(n * 3) - 5, @now) AS createdAt,
        CASE WHEN n % 10 <> 0 THEN DATEADD(MINUTE, -(n * 3) + 65, @now) ELSE NULL END AS updatedAt,
        @checksum
    FROM gen g
    CROSS APPLY (SELECT verb FROM (
        SELECT verb, ROW_NUMBER() OVER (ORDER BY verb) AS rn FROM @titleVerbs
    ) tvx WHERE tvx.rn = (g.r3 % 20) + 1) tv;

    SET @p += @PROPOSITION_BATCH;
END;

