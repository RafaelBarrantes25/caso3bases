    FROM propositionVotes
    WHERE propositionId BETWEEN @minPropId AND @maxPropId
    GROUP BY propositionId
) v ON v.propositionId = pr.id
WHERE pr.id BETWEEN @minPropId AND @maxPropId
  AND pr.propositionStatusId IN (@statusSelected, @statusAccepted, @statusRejected, @statusActive, @statusCompleted, @statusCancelled, @statusUnverifiable);

