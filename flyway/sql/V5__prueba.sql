/*
    Gathel - V5__bulk_seed_data.sql
    Database engine: SQL Server
    Database name: Gathel

    Flyway versioned migration.

    Purpose:
    - Bulk seeding script for development/testing environments.
    - Guarantees every team member gets the exact same initial dataset
      on top of the V1-V4 migrations (schema + catalogs + base seed).

    Generates (minimum):
    - 1000 players (users with role PLAYER)
    - 5000 propositions
    - 250000 events (logs) associated to propositions
    - Payment attempts + validations + transactions + predictions
      for the 5000 propositions

    Design:
    - All data is generated with set-based batched loops (no exhaustive
      literal INSERTs).
    - Referential integrity is guaranteed: every FK is resolved from
      previously inserted rows.
    - Deterministic pseudo-random generation via CHECKSUM/RAND seeded
      from row numbers, so the dataset is reproducible across environments
      (same row counts, same distribution shapes -> same statistics,
      though exact RAND() values are not guaranteed bit-identical across
      SQL Server versions; row counts and relationships ARE identical).
    - Timestamps are coherent: createdAt values move forward in time and
      dependent rows (votes, status changes, payments, transactions,
      predictions, results) are always timestamped after their parent.
    - Distribution of events, proposition statuses, payment statuses and
      prediction outcomes is varied using modulo-based buckets driven by
      the row counter, giving a realistic, non-uniform spread.

    Important note:
    - This migration is intended for local/dev seeding only. It should
      NOT be applied against production environments.
*/

