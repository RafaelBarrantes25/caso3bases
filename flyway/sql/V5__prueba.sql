/* 4.3: Transactions - only for APPROVED payment attempts (deduct from PTS wallet) */
;WITH approvedPayments AS (
    SELECT
        pa.id AS paymentAttemptId,
        pa.userId,
        pa.amount,
        pa.referenceObjectId AS propositionId,
        pa.createdAt,
        w.id AS walletId,
        w.balance AS currentBalance,
        ROW_NUMBER() OVER (ORDER BY pa.id) AS rn
    FROM paymentAttempts pa
    INNER JOIN wallets w ON w.userId = pa.userId AND w.currencyId = @ptsId
    WHERE pa.id BETWEEN @minPayId AND @maxPayId
      AND pa.paymentStatusId = @payStatusApproved
)
UPDATE w
SET balance = w.balance - ap.amount,
    updatedAt = ap.createdAt
FROM wallets w
INNER JOIN approvedPayments ap ON ap.walletId = w.id;

INSERT INTO transactions (
    transactionTypeId, walletId, paymentAttemptId, currencyId, exchangeRateId,
    amount, balanceBefore, balanceAfter, referenceType, referenceId, externalReference,
    description, createdBy, createdAt, checksum
)
SELECT
    @txTypeBet,
    w.id,
    pa.id,
    @ptsId,
    NULL,
    -pa.amount,
    w.balance + pa.amount,
    w.balance,
    'PROPOSITION',
    pa.referenceObjectId,
    pa.transactionReference,
    'Bulk seed prediction bet using Gathel points',
    pa.userId,
    DATEADD(SECOND, 2, pa.completedAt),
    @checksum
FROM paymentAttempts pa
INNER JOIN wallets w ON w.userId = pa.userId AND w.currencyId = @ptsId
WHERE pa.id BETWEEN @minPayId AND @maxPayId
  AND pa.paymentStatusId = @payStatusApproved;

/* 4.4: Predictions - one per APPROVED payment attempt, referencing the bet transaction */
INSERT INTO predictions (
    predictionTypeId, predictedBy, propositionId, walletId, betTransactionId, rewardTransactionId,
    amount, winnerAmount, winner, enabled, createdAt, checksum
)
SELECT
    CASE WHEN pr.id % 2 = 0 THEN @predTypeWill ELSE @predTypeWillNot END,
    pa.userId,
    pa.referenceObjectId,
    t.walletId,
    t.id,
    NULL,
    pa.amount,
    NULL,
    CASE
        WHEN pr.propositionStatusId IN (@statusCompleted, @statusUnverifiable, @statusCancelled, @statusRejected) THEN
            CASE
                WHEN (pr.propositionStatusId = @statusCompleted AND pr.id % 2 = 0) THEN 1
                WHEN (pr.propositionStatusId IN (@statusUnverifiable, @statusCancelled, @statusRejected) AND pr.id % 2 = 1) THEN 1
                ELSE 0
            END
        ELSE NULL
    END,
    1,
    DATEADD(SECOND, 3, pa.completedAt),
    @checksum
FROM paymentAttempts pa
INNER JOIN transactions t ON t.paymentAttemptId = pa.id
INNER JOIN propositions pr ON pr.id = pa.referenceObjectId
WHERE pa.id BETWEEN @minPayId AND @maxPayId
  AND pa.paymentStatusId = @payStatusApproved;

/* 4.5: Reward transactions + winnerAmount for winning predictions on resolved propositions */
;WITH winners AS (
    SELECT
        pred.id AS predictionId,
        pred.walletId,
        pred.amount,
        w.balance AS currentBalance,
        pa.id AS paymentAttemptId
    FROM predictions pred
    INNER JOIN wallets w ON w.id = pred.walletId
    INNER JOIN transactions bt ON bt.id = pred.betTransactionId
    INNER JOIN paymentAttempts pa ON pa.id = bt.paymentAttemptId
    WHERE pred.winner = 1
      AND pa.id BETWEEN @minPayId AND @maxPayId
)
UPDATE w
SET balance = w.balance + win.amount * 2,
    updatedAt = @now
FROM wallets w
INNER JOIN winners win ON win.walletId = w.id;

INSERT INTO transactions (
    transactionTypeId, walletId, paymentAttemptId, currencyId, exchangeRateId,
    amount, balanceBefore, balanceAfter, referenceType, referenceId, externalReference,
    description, createdBy, createdAt, checksum
)
SELECT
    @txTypeReward,
    w.id,
    bt.paymentAttemptId,
    @ptsId,
    NULL,
    pred.amount * 2,
    w.balance - (pred.amount * 2),
    w.balance,
    'PROPOSITION',
    pred.propositionId,
    CONCAT('tx_seed_reward_', pred.id),
    'Bulk seed reward payment for winning prediction',
    pred.predictedBy,
    DATEADD(MINUTE, 10, bt.createdAt),
    @checksum
FROM predictions pred
INNER JOIN transactions bt ON bt.id = pred.betTransactionId
INNER JOIN wallets w ON w.id = pred.walletId
WHERE pred.winner = 1
  AND bt.paymentAttemptId BETWEEN @minPayId AND @maxPayId;

UPDATE pred
SET pred.rewardTransactionId = rt.id,
    pred.winnerAmount = pred.amount * 2
FROM predictions pred
INNER JOIN transactions bt ON bt.id = pred.betTransactionId
INNER JOIN transactions rt ON rt.referenceId = pred.propositionId
                            AND rt.transactionTypeId = @txTypeReward
                            AND rt.walletId = pred.walletId
WHERE pred.winner = 1
  AND bt.paymentAttemptId BETWEEN @minPayId AND @maxPayId;

/* ==========================================================
   STEP 5: 250,000 EVENTS (LOGS) ASSOCIATED TO PROPOSITIONS
   50 events per proposition (5000 * 50 = 250,000)
   Distributed across log types, event types, severities and sources.
   ========================================================== */
PRINT 'Seeding proposition events (logs)...';

DECLARE @batchProp INT = @minPropId;

WHILE @batchProp <= @maxPropId
BEGIN
    ;WITH batchProps AS (
        SELECT pr.id AS propositionId, pr.proposedBy, pr.proposedTo, pr.createdAt
        FROM propositions pr
        WHERE pr.id >= @batchProp AND pr.id < @batchProp + @EVENT_BATCH_PROPOSITIONS
          AND pr.id <= @maxPropId
    ),
    eventSeq AS (
        SELECT n AS k
        FROM #Numbers
        WHERE n <= @EVENTS_PER_PROPOSITION
    )
    INSERT INTO logs (
        logTypeId, eventTypeId, severityId, sourceId, dataObjectId, description,
        objectId1, objectId2, referenceId1, referenceId2, referenceDescription,
        userId, computer, checksum, postTime
    )
    SELECT
        CASE (bp.propositionId * 50 + es.k) % 5
            WHEN 0 THEN @logTypeUser
            WHEN 1 THEN @logTypeAI
            WHEN 2 THEN @logTypeSystem
            WHEN 3 THEN @logTypePayment
            ELSE @logTypeSecurity
        END AS logTypeId,
        CASE (bp.propositionId * 50 + es.k) % 10
            WHEN 0 THEN @evtLogin
            WHEN 1 THEN @evtLogout
            WHEN 2 THEN @evtInsert
            WHEN 3 THEN @evtUpdate
            WHEN 4 THEN @evtDelete
            WHEN 5 THEN @evtPaymentAttempt
            WHEN 6 THEN @evtAiReview
            WHEN 7 THEN @evtTransaction
            WHEN 8 THEN @evtPropositionCreated
            ELSE @evtPredictionCreated
        END AS eventTypeId,
        CASE (bp.propositionId * 50 + es.k) % 20
            WHEN 0 THEN @sevCritical
            WHEN 1 THEN @sevError
            WHEN 2 THEN @sevWarning
            WHEN 3 THEN @sevWarning
            ELSE @sevInfo
        END AS severityId,
        CASE (bp.propositionId * 50 + es.k) % 5
            WHEN 0 THEN @srcFrontend
            WHEN 1 THEN @srcBackend
            WHEN 2 THEN @srcDatabase
            WHEN 3 THEN @srcAiProvider
            ELSE @srcPaymentProvider
        END AS sourceId,
        CASE (bp.propositionId * 50 + es.k) % 6
            WHEN 0 THEN @doUsers
            WHEN 1 THEN @doPropositions
            WHEN 2 THEN @doPredictions
            WHEN 3 THEN @doPaymentAttempts
            WHEN 4 THEN @doTransactions
            ELSE @doAiProcessLogs
        END AS dataObjectId,
        CONCAT('Bulk seed event #', es.k, ' for proposition ', bp.propositionId) AS description,
        bp.propositionId AS objectId1,
        NULL AS objectId2,
        bp.propositionId AS referenceId1,
        NULL AS referenceId2,
        'Auto-generated event for proposition lifecycle (seed batch)' AS referenceDescription,
        CASE WHEN es.k % 2 = 0 THEN bp.proposedBy ELSE bp.proposedTo END AS userId,
        NULL AS computer,
        @checksum,
        DATEADD(MINUTE, es.k, bp.createdAt) AS postTime
    FROM batchProps bp
    CROSS JOIN eventSeq es;

    SET @batchProp += @EVENT_BATCH_PROPOSITIONS;
END;

