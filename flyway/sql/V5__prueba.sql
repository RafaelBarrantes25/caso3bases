    CASE (pr.id % 12)
        WHEN 2 THEN 'ERR_PROVIDER_TIMEOUT'
        WHEN 4 THEN 'ERR_INSUFFICIENT_FUNDS'
        ELSE NULL
    END,
    DATEADD(SECOND, 30, pr.createdAt),
    CASE (pr.id % 12)
        WHEN 0 THEN NULL
        ELSE DATEADD(SECOND, 45, pr.createdAt)
    END,
    @checksum
FROM propositions pr
WHERE pr.id BETWEEN @minPropId AND @maxPropId;

