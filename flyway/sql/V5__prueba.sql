INSERT INTO propositionVotes (userId, propositionId, votedAt, checksum)
SELECT DISTINCT
    @minPlayerId + ((ABS(CHECKSUM(pr.id, n.k, 'V')) % @playerCount)) AS userId,
    pr.id,
    DATEADD(MINUTE, -((pr.id % 1000)) + (n.k * 2), pr.votingStartsAt),
    @checksum
FROM propositions pr
CROSS JOIN (SELECT 1 AS k UNION ALL SELECT 2 UNION ALL SELECT 3) n
